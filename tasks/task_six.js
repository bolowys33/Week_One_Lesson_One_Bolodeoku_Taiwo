// Conversion of string to numbers

let number = '25';

console.log(number);
console.log(typeof(number));

let number2 = parseInt(number);
console.log(number2);
console.log(typeof(number2));

// Conversion of Numbers to strings

number = 20;

console.log(number);
console.log(typeof(number));

number2 = number.toString();
console.log(number2);
console.log(typeof(number2));

